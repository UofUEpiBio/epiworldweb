<h1>Welcome to EpiworldWeb!</h1>
<p>
	EpiworldWeb is the web wrapper around the
	<a href="https://github.com/UofUEpiBio/epiworld">Epiworld</a> library from the
	<a href="https://medicine.utah.edu/internal-medicine/epidemiology">Division of Epidemiology</a>
	at the University of Utah. To get started, fill out the information boxes presented to you in the 'property
	editor' to you to your left, and press 'run.' You'll be able to interact with the model data once the
	model finishes simulating through an R/Python REPL, or manually. EpiworldWeb runs entirely in your
	browser.
</p>
